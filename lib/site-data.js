export function createMetadata({
  title,
  description,
  path = "/",
  image = siteConfig.defaultOgImage,
  keywords = [],
  type = "website"
}) {
  const canonicalUrl = path === "/" ? siteConfig.siteUrl : `${siteConfig.siteUrl}${path}`;

  return {
    title,
    description,
    keywords: [...siteConfig.keywords, ...keywords],
    alternates: {
      canonical: canonicalUrl
    },
    robots: {
      index: true,
      follow: true,
      googleBot: {
        index: true,
        follow: true,
        "max-image-preview": "large",
        "max-snippet": -1,
        "max-video-preview": -1
      }
    },
    openGraph: {
      title,
      description,
      url: canonicalUrl,
      siteName: siteConfig.name,
      locale: siteConfig.locale,
      type,
      images: [{ url: image }]
    },
    twitter: {
      card: "summary_large_image",
      title,
      description,
      images: [image]
    },
    other: {
      "geo.region": "SK-TC",
      "geo.placename": `${siteConfig.addressLocality}, Slovensko`,
      "geo.position": `${siteConfig.coordinates.latitude};${siteConfig.coordinates.longitude}`,
      ICBM: `${siteConfig.coordinates.latitude}, ${siteConfig.coordinates.longitude}`
    }
  };
}
